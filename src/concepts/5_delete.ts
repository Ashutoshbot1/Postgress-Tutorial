/*
 * 1. Delete data from table
 * DELETE FROM table_name WHERE condition;
 *
 * Example:
 * DELETE FROM users WHERE id = 1;
 */

/*
 * 2. Delete multiple columns from table
 * DELETE FROM table_name WHERE condition;
 *
 * Example:
 * DELETE FROM users WHERE id = 1;
 */

/*
 * 3. Delete data from table with multiple conditions
 * DELETE FROM table_name WHERE condition1 AND condition2;
 *
 * Example:
 * DELETE FROM users WHERE id = 1 AND name = 'John Doe';
 */

/*
 * 4. Delete data from table with OR condition
 * DELETE FROM table_name WHERE condition1 OR condition2;
 *
 * Example:
 * DELETE FROM users WHERE id = 1 OR name = 'John Doe';
 */

/*
 * 5. Delete data from table with NOT condition
 * DELETE FROM table_name WHERE NOT condition;
 *
 * Example:
 * DELETE FROM users WHERE NOT id = 1;
 */

/*
 * 6. Delete data from table with IN condition
 * DELETE FROM table_name WHERE column IN (value1, value2, value3);
 *
 * Example:
 * DELETE FROM users WHERE id IN (1, 2, 3);
 */

/*
 * 7. Delete all data from table
 * DELETE FROM table_name;
 *
 * Example:
 * DELETE FROM users;
 */
