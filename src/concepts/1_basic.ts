/*
 * DBMS (Database Management System) vs Database.
 * DBMS is software that manage database.
 * Database is collection of data.
 * Few examples of DBMS are MySQL, PostgreSQL, MongoDB, etc.
 */

/*
 * RDBMS (Relational Database Management System).
 * A type of database that stores data in tables(using rows and columns) and uses SQL (structured query language) to manage data.
 * Few examples of RDBMS are MySQL, PostgreSQL, Oracle, etc.
 */

/*
 * SQL (Structured Query Language).
 * A programming language used to manage and interact with databases.
 * It is used to create, read, update, and delete data in a database.
 */
